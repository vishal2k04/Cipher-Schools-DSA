public class Operators{
    public static void main(String args[]){
        
        float f =(float)5.5;
        System.out.println(f);
        float f2 = 5.5f;
        System.out.println(f2);
        char ch = 'a';
        System.out.println(ch);
        //int z=x+y;
        // + -> operator 
        // x,y -> operands
        int m=5;
        int n=m++;
        System.out.println("M is: "+m+",N is: "+n);

        int p=5;
        int q=++p;
        System.out.println("P is: "+p+", Q is: "+q);

    }
}